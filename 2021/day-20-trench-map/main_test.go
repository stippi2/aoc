var example = `###..

#..#.
#....
##..#`

func Test_parseInput(t *testing.T) {
	a, i := parseInput(example)
	assert.Equal(t, "###..", a)

	expectedImage := newImage()
	expectedImage.set(0,0)
	expectedImage.set(3,0)
	expectedImage.set(0,1)
	expectedImage.set(0,2)
	expectedImage.set(1,2)
	expectedImage.set(4,2)

	assert.Equal(t, expectedImage, i)
}

